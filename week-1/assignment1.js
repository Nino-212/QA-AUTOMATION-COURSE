// Declaring a variable:

let age;

// Assigning a value to the variable:

age = 25;

// Reassigning a value to the variable:

age = 26;

// output the current age value after reassignment:

console.log(age);

// Declaring a constant (CANNOT be reassigned ):

const birthYear = 2001;

console.log(birthYear);

//Variable named using camelCase:

const myBirthCountryAndCity = "Uzbekistan, Samarkand";

console.log(myBirthCountryAndCity);

